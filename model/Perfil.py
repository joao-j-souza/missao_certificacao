class Perfil:
    def setNome(self, nome):
        self.nome = nome

    def getNome():
        return self.nome
    
    def setDescricao(self, descricao):
        self.descricao = descricao

    def getDescricao():
        return self.descricao
    
    def setCodSistema(self, cod_sistema):
        self.cod_sistema = cod_sistema

    def getCodSistema():
        return self.cod_sistema
    
    